    anns = []
    edges= []
    for sample in batch:
        imgs.append(torch.from_numpy(sample[0]).permute(2,0,1))
        anns.append(torch.from_numpy(sample[2]))
        edges.append(torch.from_numpy(sample[2]))
    anns = torch.stack(anns)
    anns.map_(anns, lambda x, y: cat_ids[x])
